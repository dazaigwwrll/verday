function initialLang(): Lang {
  const saved = localStorage.getItem(KEY);
  return saved === "en" ? "en" : "fa";
}

function initialCal(): CalSystem {
  const saved = localStorage.getItem(CAL_KEY);
  return saved === "gregorian" || saved === "both" ? saved : "jalali";
}

interface I18nValue {
  lang: Lang;
  dir: "rtl" | "ltr";
  setLang: (l: Lang) => void;
  cal: CalSystem;
  setCal: (c: CalSystem) => void;
  t: (key: string, params?: Record<string, string | number>) => string;
  moodMsgs: (level: number) => string[];
  sleepMsgs: (category: string) => string[];
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>(initialLang);
  const [cal, setCal] = useState<CalSystem>(initialCal);

  // keep the date layer in sync synchronously, before children render
  setDateLocale(lang);
  setDateCalendar(cal);

  useEffect(() => {
    document.documentElement.setAttribute("lang", lang);
    document.documentElement.setAttribute("dir", lang === "fa" ? "rtl" : "ltr");
    localStorage.setItem(KEY, lang);
  }, [lang]);

  useEffect(() => {
    localStorage.setItem(CAL_KEY, cal);
  }, [cal]);

  const t = (key: string, params?: Record<string, string | number>) => {
    let s = DICTS[lang][key] ?? DICTS.fa[key] ?? key;
    if (params)
      for (const [k, v] of Object.entries(params))
        s = s.replace(`{${k}}`, String(v));
    return s;
  };

  const moodMsgs = (level: number) => MOOD_MSGS[lang][level] ?? [];
  const sleepMsgs = (category: string) => SLEEP_MSGS[lang][category] ?? [];

  return (
    <I18nContext.Provider
      value={{
        lang,
        dir: lang === "fa" ? "rtl" : "ltr",
        setLang,
        cal,
        setCal,
        t,
        moodMsgs,
        sleepMsgs,
      }}
    >
      {children}
    </I18nContext.Provider>
  );
}

// eslint-disable-next-line react-refresh/only-export-components
export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used within I18nProvider");
  return ctx;
}
